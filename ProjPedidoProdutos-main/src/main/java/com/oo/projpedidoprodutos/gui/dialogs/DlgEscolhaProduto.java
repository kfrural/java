    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

        String idEscolhido = edtId.getText();        
        if (!idEscolhido.isEmpty()) {
            produtoEscolhido = this.gerenteProdutos.buscarProduto(idEscolhido);

            if (produtoEscolhido == null) {
                JOptionPane.showMessageDialog(this, "NÃ£o existe este ID.");
                lblProdutoSelecionado.setText("");
            } else {
                lblProdutoSelecionado.setText(produtoEscolhido.toString());
            }
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnAddProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProdutoActionPerformed
        if (produtoEscolhido == null) {
            int response = JOptionPane.showConfirmDialog(null,
                    "Nenhum produto foi escolhido. Deseja continuar?",
                    "Confirm",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.OK_OPTION) {
                dispose();
            }
        } else {
            this.pedido.addProduto(produtoEscolhido);
            dispose();
        }
    }//GEN-LAST:event_btnAddProdutoActionPerformed

