    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String produtoEscolhida = JOptionPane.showInputDialog("Informe o nome que vc deseja remover");
        
        Produto p = gerenciador.buscarProduto(produtoEscolhida);
        
        if(p == null)
            JOptionPane.showMessageDialog(this, "Nao tem essa produto");
        else{
            gerenciador.removerProduto(produtoEscolhida);
            JOptionPane.showMessageDialog(this, "Produto excluida com sucesso");
        }
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Produto p = this.camposParaObj();
        
        if(this.editando)
            gerenciador.atualizarProduto(nomeAntigo, p);
        else
            gerenciador.adicionarProduto(p);
        
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;
        
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
        
        gerenciador.salvarArquivo("ListagemProduto.csv");
    }//GEN-LAST:event_btnSalvarActionPerformed


