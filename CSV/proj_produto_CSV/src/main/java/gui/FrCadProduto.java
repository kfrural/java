    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String nomeEscolhido = JOptionPane.showInputDialog("Informe o nome q ce quer editar: ");
        
        Produto pEditando = gerenciador.buscarProduto(nomeEscolhido);
        
        if(pEditando == null){
            JOptionPane.showMessageDialog(this, "Nao tem essa produto");
            this.editando = false;
        } else{
            this.limparCampos();
            this.habilitarCampos(true);
            this.objParaCampos(pEditando);
            this.editando = true;
            this.nomeAntigo = pEditando.getNome();
        }
    }//GEN-LAST:event_btnEditActionPerformed

