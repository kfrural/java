    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String casaEscolhida = JOptionPane.showInputDialog("Informe o cod que vc deseja remover");
        
        Casa p = gerenciador.buscarCasa(casaEscolhida);
        
        if(p == null)
            JOptionPane.showMessageDialog(this, "Nao tem essa casa");
        else{
            gerenciador.removerCasa(casaEscolhida);
            JOptionPane.showMessageDialog(this, "Casa excluida com sucesso");
        }
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Casa p = this.camposParaObj();
        
        if(this.editando)
            gerenciador.atualizarCasa(nomeAntigo, p);
        else
            gerenciador.adicionarCasa(p);
        
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;
        
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
        
        gerenciador.salvarArquivo("ListagemCasa.csv");
    }//GEN-LAST:event_btnSalvarActionPerformed


