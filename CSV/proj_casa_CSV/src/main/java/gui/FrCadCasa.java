    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String codEscolhido = JOptionPane.showInputDialog("Informe o cod q ce quer editar: ");
        
        Casa cEditando = gerenciador.buscarCasa(codEscolhido);
        
        if(cEditando == null){
            JOptionPane.showMessageDialog(this, "Nao tem essa casa");
            this.editando = false;
        } else{
            this.limparCampos();
            this.habilitarCampos(true);
            this.objParaCampos(cEditando);
            this.editando = true;
            this.nomeAntigo = cEditando.getCod();
        }
    }//GEN-LAST:event_btnEditActionPerformed

