    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String tituloEscolhido = JOptionPane.showInputDialog("Informe o titulo q ce quer editar: ");
        
        Musica mEditando = gerenciador.buscarMusica(tituloEscolhido);
        
        if(mEditando == null){
            JOptionPane.showMessageDialog(this, "Nao tem essa musica");
            this.editando = false;
        } else{
            this.limparCampos();
            this.habilitarCampos(true);
            this.objParaCampos(mEditando);
            this.editando = true;
            this.tituloAntigo = mEditando.getTitulo();
        }
    }//GEN-LAST:event_btnEditActionPerformed

