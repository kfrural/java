    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String musicaEscolhida = JOptionPane.showInputDialog("Informe o titulo que vc deseja remover");
        
        Musica m = gerenciador.buscarMusica(musicaEscolhida);
        
        if(m == null)
            JOptionPane.showMessageDialog(this, "Nao tem essa musica");
        else{
            gerenciador.removerMusica(musicaEscolhida);
            JOptionPane.showMessageDialog(this, "Musica excluida com sucesso");
        }
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Musica m = this.camposParaObj();
        
        if(this.editando)
            gerenciador.atualizarMusica(tituloAntigo, m);
        else
            gerenciador.adicionarMusica(m);
        
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;
        
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
        
        gerenciador.salvarArquivo("ListagemMusica.csv");
    }//GEN-LAST:event_btnSalvarActionPerformed


