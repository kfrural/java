    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String carroEscolhida = JOptionPane.showInputDialog("Informe o cod que vc deseja remover");
        
        Carro p = gerenciador.buscarCarro(carroEscolhida);
        
        if(p == null)
            JOptionPane.showMessageDialog(this, "Nao tem essa carro");
        else{
            gerenciador.removerCarro(carroEscolhida);
            JOptionPane.showMessageDialog(this, "Carro excluida com sucesso");
        }
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Carro p = this.camposParaObj();
        
        if(this.editando)
            gerenciador.atualizarCarro(nomeAntigo, p);
        else
            gerenciador.adicionarCarro(p);
        
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;
        
        String listagem = gerenciador.toString();
        edtListagem.setText(listagem);
        
        gerenciador.salvarArquivo("ListagemCarro.csv");
    }//GEN-LAST:event_btnSalvarActionPerformed


