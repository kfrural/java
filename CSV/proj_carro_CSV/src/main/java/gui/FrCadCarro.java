    public void limparCampos(){
        edtCod.setText("");
        edtMarca.setText("");
        edtModelo.setText("");
        edtAno.setText("");
        edtPreco.setText("");
    }
    
    public void objParaCampos(Carro carro){
        edtCod.setText(carro.getCod());
        edtMarca.setText(carro.getMarca());
        edtModelo.setText(carro.getModelo());
        edtAno.setText(carro.getAno() + "");
        edtPreco.setText(carro.getPreco() + "");
    }
    
    public Carro camposParaObj(){
        Carro c = new Carro();
        c.setCod(edtCod.getText());
        c.setMarca(edtMarca.getText());
        c.setModelo(edtModelo.getText());
        String AnoStr = edtAno.getText();
        int i = 0;
        if(!AnoStr.isEmpty())
            i = Integer.parseInt(AnoStr);
        c.setAno(i);
        
        String precoStr = edtPreco.getText();
        double preco = 0;
        if(!precoStr.isEmpty())
            preco = Double.parseDouble(precoStr);
        c.setPreco(preco);
        
        return c;   
    }

